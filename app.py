# List
mixed = [1, 2, 3, 4, 5, "Hello", [1, 2, 3]]
print(mixed)
print(type(mixed))

# Dictionary
user_info = {"user_name": "Light", "user_id": 1}
print(user_info)
print(type(user_info))

# Tuple
mixed_tuple = (1, 2, 3, 4, 5, "Hello", [1, 2, 3])
print(mixed_tuple)
print(type(mixed_tuple))

# Set
mixed_set = {1, 2, 3.3, 5.3, "Python", "Abc", 101}
print(mixed_set)
print(type(mixed_set))
